#define     RX_FRAME_FILTER_CNT     10
static uint8_t g_RxFrameFilterCnt = 0;
int8_t Voice_Open(uint8_t sampleBits, uint16_t framSize, uint16_t sampleRate)
{
    I2S_BusCfg busCfg = {0};
    I2S_TimingCfg i2sTimingCfg = {0};

    i2sTimingCfg.chnl = I2S_LEFT_CHNL;
    i2sTimingCfg.dataBits = sampleBits;
    i2sTimingCfg.dataCycle = DATA_32_BITS;

    busCfg.workMode = I2S_PLAY_MODE | I2S_RECORD_MODE;
    busCfg.timing = TIMING_I2S;
    busCfg.frameSize = framSize;
    busCfg.timingCfg = &i2sTimingCfg;
    busCfg.sampleRate = sampleRate;
    busCfg.fs = Codec_GetFs(busCfg.sampleRate);

    VoiceProcFunc voiceFunc = {0};
#if defined(USE_MOBVOI_DSP)
    voiceFunc.init = VoiceProc_Initialize;
    voiceFunc.deinit = VoiceProc_UnInitialize;
    voiceFunc.rx_proc_notify = VoiceProc_SendMsg;
    voiceFunc.tx_proc = VoiceProc_TxProc;
#endif

    if(I2S_Initialize(&busCfg, NULL, &voiceFunc) < 0)   /** I2S Init   */
    {
        osPrintf("i2s init failed \r\n");
        return VOICE_ERR;
    }

    if(Codec_Initialize(sampleRate, DATA_16_BITS) < 0)           /** Codec Init */
        return VOICE_ERR_UNSUPPORTED;
        
    return VOICE_OK;
}

int8_t Voice_Close(void)
{
    I2S_UnInitialize();
    Codec_UnInitialize();
    return VOICE_OK;
}

