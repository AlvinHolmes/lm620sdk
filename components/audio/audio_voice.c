static osThreadId_t g_ringTonetaskHandle = NULL;
static struct osSemaphore *g_ringToneSem = NULL;
static uint8_t *g_currentRingToneData = (uint8_t *)ringToneAmrData;
static uint32_t g_currentRingToneDataSize = sizeof(ringToneAmrData);
static struct osSemaphore *g_ringToneStopSem = NULL;

static void Voice_LocalRingToneTask(void *para)
{
    uint8_t *decodeDataPtr = NULL;
    uint32_t sampleRate;
    uint8_t dataBits;
    uint16_t frameSize;
    void *amrHandle = NULL;
    int16_t *pPcmDec = NULL;
    uint8_t amrdecsize = 32;

    frameSize = VOICE_NB_FRAME_SIZE;
    sampleRate = VOICE_NB_SAMPLE_RATE;
    dataBits = VOICE_DATA_BITS_PER_SAMPLE;

    amrHandle = Decoder_Interface_init();
    if(!amrHandle)
    {
        osPrintf("local ring amr decode init failed \r\n");
        return ;
    }

    Voice_Open(dataBits, frameSize, sampleRate);
    Voice_WriteStart();
    pPcmDec = (int16_t *)osMalloc(VOICE_NB_FRAME_SIZE);
    decodeDataPtr = g_currentRingToneData + 6;

    while(true)
    {
        if(osSemaphoreAcquire(g_ringToneSem, osNoWait) == osOK)
        {
            break;
        }

        Decoder_Interface_Decode(amrHandle, decodeDataPtr, pPcmDec, 2);
        Voice_WriteOneFrame((uint8_t *)pPcmDec, VOICE_NB_FRAME_SIZE, osWaitForever);
        decodeDataPtr += amrdecsize;
        if(decodeDataPtr > (g_currentRingToneData + g_currentRingToneDataSize))
        {
            decodeDataPtr = g_currentRingToneData + 6;
        }
    } 

    // task quit
    if(pPcmDec)
        osFree(pPcmDec);

    Voice_WriteStop();
    Voice_Close();

    Decoder_Interface_exit(amrHandle);

    osSemaphoreDetach(g_ringToneSem);
    osFree(g_ringToneSem);
    g_ringToneSem = NULL;
    g_ringTonetaskHandle = NULL;

    osSemaphoreRelease(g_ringToneStopSem);
}

int8_t Voice_StartLocalRingTone(void)
{
    if(!g_ringToneSem)
    {
        g_ringToneSem = osMalloc(sizeof(struct osSemaphore));
        OS_ASSERT(g_ringToneSem);
        osSemaphoreInit(g_ringToneSem, 0, 1, OS_IPC_FLAG_FIFO); 
    }

    if(!g_ringToneStopSem)
    {
        g_ringToneStopSem = osMalloc(sizeof(struct osSemaphore));
        OS_ASSERT(g_ringToneStopSem);
        osSemaphoreInit(g_ringToneStopSem, 0, 1, OS_IPC_FLAG_FIFO); 
    }
    
    if(!g_ringTonetaskHandle)
    {
        osThreadAttr_t attr = {"ring-tone", osThreadDetached, NULL, 0U, NULL, 4*1024, osPriorityISR - 20, 0U, 0U};
        g_ringTonetaskHandle = osThreadNew((osThreadFunc_t)Voice_LocalRingToneTask, NULL, &attr);
        OS_ASSERT(g_ringTonetaskHandle);
    }


    return VOICE_OK;
}

int8_t Voice_StopLocalRingTone(void)
{
    if(g_ringTonetaskHandle)
    {
        if(g_ringToneSem)
            osSemaphoreRelease(g_ringToneSem);
        
        osSemaphoreAcquire(g_ringToneStopSem, osWaitForever);     //wait task quit
    }

    return VOICE_OK;
}

