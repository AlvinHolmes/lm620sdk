int8_t Voice_WriteStart(void)
{
    g_RxFrameFilterCnt = 0;
    Codec_Start(CODEC_PLAY | CODEC_RECORD);
    I2S_BusStart(I2S_PLAY_MODE | I2S_RECORD_MODE);
    return VOICE_OK;
}

int8_t Voice_WriteStop(void)
{
    g_RxFrameFilterCnt = 0;
    I2S_BusStop(I2S_PLAY_MODE | I2S_RECORD_MODE);
    Codec_Stop(CODEC_PLAY | CODEC_RECORD);
    return VOICE_OK;
}

int8_t Voice_WriteOneFrame(uint8_t *frameBuf, uint16_t frameSize, uint32_t timeOut)
{
    if(I2S_BusWrite(frameBuf, frameSize, timeOut) > 0)
    {
        return VOICE_OK;
    }

    return VOICE_ERR;
}

int8_t Voice_ReadOneFrame(uint8_t *frameBuf, uint16_t frameSize, uint32_t timeOut)
{
    if(I2S_BusRead(frameBuf, frameSize, timeOut) > 0)
    {
        if(g_RxFrameFilterCnt < RX_FRAME_FILTER_CNT)
        {
            g_RxFrameFilterCnt ++;
            memset(frameBuf, 0, frameSize);
        }
        return VOICE_OK;
    }
    
    return VOICE_ERR;
}

