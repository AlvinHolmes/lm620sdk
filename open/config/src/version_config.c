/**
 ************************************************************************************
 * @brief       获取编译时间, 格式: YYYYMMDDhhmmss
 ************************************************************************************
 */
const char* VER_GetBuildTime(char *buf, uint32_t len)
{
    osSnprintf(buf, len, "%d%02d%02d", BUILD_YEAR, BUILD_MONTH, BUILD_DAY);
    if (len >= 15) {
        const char *time = __TIME__;    /* __TIME__ format: HH:MM:SS */
        memcpy(&buf[8], &time[0], 2);
        memcpy(&buf[10], &time[3], 2);
        memcpy(&buf[12], &time[6], 2);
        buf[14] = 0;
    }
    return buf;
}

/**
 ************************************************************************************
 * @brief       获取设备内部软件版本号
 ************************************************************************************
 */
const char* VER_GetBuildVersion(char *buf, uint32_t len)
{
    static const char buildVer[VERSION_NUMBER_LEN] = BUILD_VERSION;
    if (len >= sizeof(buildVer)+1)
        len = sizeof(buildVer)+1;
    strncpy(buf, buildVer, len);
    buf[len-1] =  '\0';
    return buf;
}

